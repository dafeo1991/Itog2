package Lesson_02.Homework2;

public class Toy {
    private String numToy;
    private double weight;

    public Toy(String numToy, double weight) {
        this.numToy = numToy;
        this.weight = weight;
    }


    public String getNumToy() {
        return numToy;
    }

    public double getWeight() {
        return weight;
    }


}